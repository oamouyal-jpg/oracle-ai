export type Messages = WidenStrings<typeof en>;

const he: Messages = heMessages;
const fr: Messages = frMessages;

export const messages: Record<Locale, Messages> = { en, he, fr };

/** Dot-path lookup, e.g. t("nav.missions") */
function lookupMessage(locale: Locale, key: string): string | undefined {
  const parts = key.split(".");
  let value: unknown = messages[locale];
  for (const p of parts) {
    if (value && typeof value === "object" && p in value) {
      value = (value as Record<string, unknown>)[p];
    } else {
      return undefined;
    }
  }
  return typeof value === "string" ? value : undefined;
}

export function translate(
  locale: Locale,
  key: string,
  vars?: Record<string, string | number>
): string {
  let text = lookupMessage(locale, key);
  if (text === undefined && locale !== "en") {
    text = lookupMessage("en", key);
  }
  if (text === undefined) text = key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    }
  }
  return text;
}

export function speechLocale(locale: Locale): string {
  if (locale === "he") return "he-IL";
  if (locale === "fr") return "fr-FR";
  return "en-US";
}

export function htmlLang(locale: Locale): string {
  if (locale === "he") return "he";
  if (locale === "fr") return "fr";
  return "en";
}

export function isRtl(locale: Locale): boolean {
  return locale === "he";
}

export function getStoredLocale(): Locale {
  if (typeof window === "undefined") return "en";
  const stored = localStorage.getItem(LOCALE_STORAGE_KEY);
  if (stored === "he" || stored === "fr") return stored;
  return "en";
}
